void	ckOpt::cmdsMake(int argc, wchar_t *argv[])
{
	m_cmd.erase();
	wchar_t *chk;
	for(int i = 0 ; i < argc ; i++) {
		chk = argv[i];
		while(!wcschr(L" &()[]{}^=;!'+,`~", *chk)) chk++;
		if(*chk) {
			m_cmd += L"\"";
			m_cmd += argv[i];
			m_cmd += L"\" ";
		}
		else {
			m_cmd += argv[i];
			m_cmd += L" ";
		}
	}
}

void	ckOpt::geometry(const wchar_t *str)
{
	const wchar_t	*c = str;
	int	w = 0;
	int	h = 0;
	int	x = 0;
	int	y = 0;
	bool	minusX = false;
	bool	minusY = false;

	if(!c || *c == 0)
		return;
	for( ; *c != 0 && L'0' <= *c && *c <= L'9' ; c++)
		w = w * 10 + (*c - L'0');
	if(*c == L'x') {
		for(c++ ; *c != 0 && L'0' <= *c && *c <= L'9' ; c++)
			h = h * 10 + (*c - L'0');
	}
	if(*c != 0) {
		if(*c == L'-') { minusX = true; c++; }
		else if(*c == L'+') { c++; }
		for( ; *c != 0 && L'0' <= *c && *c <= L'9' ; c++)
			x = x * 10 + (*c - L'0');
		if(minusX) x = -x-1;

		if(*c == L'-') { minusY = true; c++; }
		else if(*c == L'+') { c++; }
		for( ; *c != 0 && L'0' <= *c && *c <= L'9' ; c++)
			y = y * 10 + (*c - L'0');
		if(minusY) y = -y-1;

		m_isWinPos = true;
		m_winPosX = x;
		m_winPosY = y;
	}
	m_winCharW = (w>0) ? w : 1;
	m_winCharH = (h>0) ? h : 1;
}

